MAIN_PROC:
		LDI R16 , 0xFF
		OUT DDRB, R16
		LDI R16 , 0
		OUT DDRC, R16
		LDI R16 , 0xFF
		OUT PORTC, R16 
		LDI R16 , 0x87
		STS ADCSRA, R16
		LDI R16, 0X40
		STS ADMUX, R16

	READ_ADC: 
			LDI R16, 0xCF
			STS ADCSRA, R16

	LOOP:	LDS R18, ADCSRA
			SBRS R18, 4
			RJMP LOOP
			LDS R18, ADCSRA
			SBR R18, 4
			STS ADCSRA,R18
			LDS R16 ,ADCL //LOW
			LDS R17 ,ADCH // HIGH
			CPI R17, 0xC8
			BRMI LIGHT_UP
			CBI PORTB, 6
			RJMP READ_ADC

LIGHT_UP:	SBI PORTB, 6
			RJMP READ_ADC
